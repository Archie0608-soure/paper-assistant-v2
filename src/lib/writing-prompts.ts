// 大纲生成提示词
export const OUTLINE_GENERATION_PROMPT = (major: string, topic: string, paperType: string) => {
  const typeDesc = paperType === 'proposal' ? '开题报告' : paperType === 'paper' ? '课程论文' : '毕业论文';
  
  return `
你是一位资深的学术论文指导教师。请为${major}专业的本科学生，针对以下论文主题，生成一份完整的${typeDesc}大纲。

论文主题：${topic}

要求：
1. 符合本科${typeDesc}的学术规范和篇幅要求
2. 章节结构合理，包括：摘要、关键词、绪论、理论基础、研究方法、结果分析、结论、参考文献
3. 每个章节简要说明内容要点（约20-50字）
4. 语言简洁、学术，避免过于口语化

请按以下JSON格式输出：
{
  "title": "论文标题",
  "chapters": [
    {"number": 1, "title": "章节标题", "content": "章节内容要点"},
    ...
  ]
}
`;
};

// 章节内容生成提示词
export const CHAPTER_WRITING_PROMPT = (chapterTitle: string, chapterContent: string, previousChapterSummary: string, topic: string) => {
  return `
你是一位在帮导师干活的研究生，导师让你帮本科生看看论文草稿。你需要"假装"帮这个本科生写一段章节内容，但实际上这段内容会被拿去检测AI率，所以必须看起来完全像人写的。

上一章已经写了：${previousChapterSummary}

现在要写的章节：${chapterTitle}
章节要求：${chapterContent}

写的时候脑子里想着：
- 一个真实的大四学生在赶ddl，凌晨两点在宿舍里敲键盘
- 边想边写，会有一些重复的废话，也会有说到一半又换说法的情况
- 专业术语会用对，但不会每句话都滴水不漏
- 偶尔会有口语化的转折，比如"然后"、"所以说"、"这个吧"

要求：
1. 字数在600-1200字左右
2. 句子长短不一，有长句也有短句
3. 可以有一两句重复表达同一个意思
4. 结尾可以不那么完美，甚至有点仓促
5. 不要每句话都那么"完美"，留点小瑕疵

直接写内容，不要加"章节标题"之类的标记。
`;
};

// 润色提示词
export const POLISH_PROMPT = (text: string) => {
  return `
你需要把下面这段学术论文内容"毁容式改写"——意思是让它看起来完全不像是AI写的，而是像一个赶deadline的本科生在凌晨两点随手敲的。

改写规则（必须全部遵守）：
1. 把"首先/其次/最后"全部换成"然后"、"接着"、"再说"
2. 把"因此/由此可见/综上所述"换成"所以说"、"这么看"、"反正"
3. 把"此外/另外/与此同时"换成"还有"、"对了"、"顺便说一句"
4. 每3句话里必须有1句是短句（5个字以内）
5. 加1-2句重复啰嗦的废话（同一个意思换词再说一遍）
6. 结尾要仓促，像是没写完或者懒得写了
7. 专业术语保留，但其他词可以口语化
8. 不要对仗工整，不要四字格

原文：
${text}

改写后（直接输出，不要加任何说明）：
`;
};
